#include "ft_printf.h"
#include "libft/libft.h"

int	print_part(const char **ps)
{
	int	i;

	i = 0;
	while ((*ps)[i] != '%' && (*ps)[i] != '\0')
		i++;
	write(1, *ps, i);
	*ps += i;
	return (i);
}
